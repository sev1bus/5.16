public class Task2 {
    public static void main(String[] args) {
        int m = 3;
        int n = 4;
        double k = Math.sqrt((m*m) + (n*n));
        int pl = (m*n)/2;
        double per = m + n + k;
        System.out.println("Площа прямокутного трикутника дорівнює: " + pl);
        System.out.println("Периметр прямокутного трикутника дорівнює: " + per);
    }
}


